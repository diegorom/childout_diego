    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DialogRecogeFirma dialogRecogeFirma = new DialogRecogeFirma(this, true);
        dialogRecogeFirma.setVisible(true);
        if (dialogRecogeFirma.getFirma() != null) {
            Blob firma = dialogRecogeFirma.getFirma();
            try {
                PreparedStatement pstmt = Conexion.conexion.prepareStatement(
                        "update autorizado set firma = ?"
                        + " where id_autorizado=1");
                pstmt.setBlob(1, firma);
                pstmt.execute();
            } catch (SQLException ex) {
                Logger.getLogger(MainPruebas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Statement stmt = Conexion.conexion.createStatement();
            ResultSet rs = stmt.executeQuery("select * from autorizado where id_autorizado=1");
            if (rs.next()) {
                Blob blobFirma = rs.getBlob("firma");
//                jLabel1.setText("");
                UtilBlob.BlobToJLabel(blobFirma, jLabel1);
            } else {
                JOptionPane.showMessageDialog(this, "Imagen no encontrada");
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainPruebas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

